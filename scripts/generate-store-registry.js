/**
 * Generate Store Registry
 *
 * Reads stores.json, finds matching themes/{name}/theme.json and page.json,
 * and generates src/generated/store-registry.ts with all imports + registries.
 *
 * Adding a new store: create themes/{name}/ with theme.json + page.json,
 * add entry to stores.json, run build. No source file edits needed.
 */

import { readFileSync, writeFileSync, existsSync, mkdirSync } from 'fs';
import { dirname, resolve } from 'path';
import { fileURLToPath } from 'url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const root = resolve(__dirname, '..');
const outDir = resolve(root, 'src/generated');
const outFile = resolve(outDir, 'store-registry.ts');

const storesConfig = JSON.parse(readFileSync(resolve(root, 'stores.json'), 'utf-8'));

// Collect unique theme names from stores.json
const themeNames = new Set();
themeNames.add(storesConfig.defaultTheme);
for (const store of Object.values(storesConfig.stores)) {
  themeNames.add(store.theme);
}

// Verify all theme directories exist
const missing = [];
for (const name of themeNames) {
  const themeDir = resolve(root, 'themes', name);
  if (!existsSync(resolve(themeDir, 'theme.json'))) missing.push(`themes/${name}/theme.json`);
  if (!existsSync(resolve(themeDir, 'page.json'))) missing.push(`themes/${name}/page.json`);
}
if (missing.length) {
  console.warn(`Warning: Missing files referenced in stores.json: ${missing.join(', ')}`);
}

// Safe variable name from theme name
function varName(name) {
  return name.replace(/[^a-zA-Z0-9]/g, '_');
}

// Generate the file
const lines = [
  '// AUTO-GENERATED by scripts/generate-store-registry.js — do not edit manually.',
  '// Run `npm run generate` or `npm run build` to rebuild.',
  '',
];

// Imports — one theme.json and one page.json per theme
for (const name of themeNames) {
  const v = varName(name);
  const themeDir = `themes/${name}`;
  if (existsSync(resolve(root, themeDir, 'theme.json'))) {
    lines.push(`import theme_${v} from '../../${themeDir}/theme.json';`);
  }
  if (existsSync(resolve(root, themeDir, 'page.json'))) {
    lines.push(`import page_${v} from '../../${themeDir}/page.json';`);
  }
}

lines.push(`import storesConfigRaw from '../../stores.json';`);
lines.push('');

// Re-export stores config
lines.push('export const storesConfig = storesConfigRaw;');
lines.push('');

// Theme registry — keyed by theme name
lines.push('export const themeRegistry: Record<string, { googleFontsUrl: string; config: any }> = {');
for (const name of themeNames) {
  if (!existsSync(resolve(root, 'themes', name, 'theme.json'))) continue;
  const v = varName(name);
  lines.push(`  '${name}': { googleFontsUrl: theme_${v}.fonts.googleFontsImport, config: theme_${v} },`);
}
lines.push('};');
lines.push('');

// Page config registry — keyed by theme name
lines.push('export const pageConfigRegistry: Record<string, any> = {');
for (const name of themeNames) {
  if (!existsSync(resolve(root, 'themes', name, 'page.json'))) continue;
  const v = varName(name);
  lines.push(`  '${name}': page_${v},`);
}
lines.push('};');
lines.push('');

// Asset hash inputs — all page configs concatenated (for cache busting)
const pcVars = [...themeNames]
  .filter(n => existsSync(resolve(root, 'themes', n, 'page.json')))
  .map(n => `page_${varName(n)}`);
lines.push(`export const allPageConfigs = [${pcVars.join(', ')}];`);
lines.push('');

// Default theme config (for UnoCSS and utility class generation)
const defaultVar = varName(storesConfig.defaultTheme);
lines.push(`export const defaultThemeConfig = theme_${defaultVar};`);
lines.push('');

// Write
mkdirSync(outDir, { recursive: true });
writeFileSync(outFile, lines.join('\n') + '\n');

const themeCount = [...themeNames].filter(n => existsSync(resolve(root, 'themes', n, 'theme.json'))).length;
console.log(`Generated store registry: ${themeCount} themes → src/generated/store-registry.ts`);

#!/usr/bin/env node
/**
 * Generate Combined Component Manifest
 *
 * Scans all _manifest.json files in the component directories and combines
 * them into a single manifest.json at the project root. This manifest is
 * given to the LLM so it knows what components and variants are available.
 *
 * Usage: node scripts/generate-manifest.js
 */
import { readFileSync, writeFileSync, readdirSync, statSync } from 'fs';
import { join, relative } from 'path';
import { fileURLToPath } from 'url';
import { dirname } from 'path';

const __filename = fileURLToPath(import.meta.url);
const __dirname = dirname(__filename);
const projectRoot = join(__dirname, '..');
const componentsDir = join(projectRoot, 'src', 'templates', 'components');

function findManifests(dir) {
  const results = [];
  const entries = readdirSync(dir);

  for (const entry of entries) {
    const fullPath = join(dir, entry);
    const stat = statSync(fullPath);

    if (stat.isDirectory()) {
      results.push(...findManifests(fullPath));
    } else if (entry === '_manifest.json') {
      results.push(fullPath);
    }
  }

  return results;
}

// Find all manifests
const manifestPaths = findManifests(componentsDir);
const components = {};

for (const manifestPath of manifestPaths) {
  const manifest = JSON.parse(readFileSync(manifestPath, 'utf8'));
  const relPath = relative(componentsDir, manifestPath);
  const domain = manifest.domain || relPath.split('/')[0];

  const key = `${domain}/${manifest.slot}`;
  components[key] = {
    slot: manifest.slot,
    domain,
    description: manifest.description,
    variants: Object.fromEntries(
      Object.entries(manifest.variants).map(([name, v]) => [
        name,
        {
          label: v.label,
          description: v.description,
        },
      ])
    ),
    default: manifest.default,
  };
}

// Read theme.json schema shape for the LLM
const themeJson = JSON.parse(readFileSync(join(projectRoot, 'theme.json'), 'utf8'));
const themeSchema = {};
for (const [key, value] of Object.entries(themeJson)) {
  if (key === '$schema' || key === 'name' || key === 'description') continue;
  if (typeof value === 'object' && !Array.isArray(value)) {
    themeSchema[key] = Object.fromEntries(
      Object.entries(value).map(([k, v]) => [k, typeof v === 'object' ? `object` : typeof v])
    );
  } else {
    themeSchema[key] = typeof value;
  }
}

// Read page.json schema shape
const pageJson = JSON.parse(readFileSync(join(projectRoot, 'page.json'), 'utf8'));

const combined = {
  _generated: new Date().toISOString(),
  _note: 'Auto-generated by scripts/generate-manifest.js. Do not edit manually.',
  components,
  themeSchema,
  pageSchema: pageJson,
};

const outputPath = join(projectRoot, 'manifest.json');
writeFileSync(outputPath, JSON.stringify(combined, null, 2));

console.log(`Generated manifest.json with ${Object.keys(components).length} component slots`);
console.log('Components:');
for (const [key, comp] of Object.entries(components)) {
  const variantCount = Object.keys(comp.variants).length;
  console.log(`  ${key}: ${variantCount} variant(s) [default: ${comp.default}]`);
}
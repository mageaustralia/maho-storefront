// Generate Plugin Registry
//
// Scans src/plugins/*/index.ts for plugin manifests and generates:
//   - src/generated/plugin-registry.ts  (SSR: slots + headScripts only, no controller imports)
//   - src/generated/plugin-controllers.ts (Client: controllers only, no JSX)
//
// Adding a new plugin: create src/plugins/<name>/index.ts exporting a PluginManifest.
// Run `npm run build`. No other source file edits needed.

import { readdirSync, readFileSync, existsSync, mkdirSync, writeFileSync } from 'fs';
import { dirname, resolve } from 'path';
import { fileURLToPath } from 'url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const root = resolve(__dirname, '..');
const pluginsDir = resolve(root, 'src/plugins');
const outDir = resolve(root, 'src/generated');

// Find all plugins with an index.ts
const pluginDirs = readdirSync(pluginsDir, { withFileTypes: true })
  .filter(d => d.isDirectory())
  .filter(d => existsSync(resolve(pluginsDir, d.name, 'index.ts')))
  .map(d => d.name)
  .sort();

function varName(name) {
  return 'plugin_' + name.replace(/[^a-zA-Z0-9]/g, '_');
}

// ---- Parse each plugin manifest to extract SSR-safe and client-side parts ----
const pluginData = [];

for (const name of pluginDirs) {
  const indexPath = resolve(pluginsDir, name, 'index.ts');
  const source = readFileSync(indexPath, 'utf-8');

  // Find component imports (for SSR): import { Foo } from './Bar';
  const componentImports = [];
  const importRegex = /import\s+(?:{[^}]+}|\w+)\s+from\s+['"]([^'"]+)['"]/g;
  let m;
  while ((m = importRegex.exec(source)) !== null) {
    const importPath = m[1];
    // Skip controller imports (they reference stimulus.js which uses CDN URLs)
    if (importPath.includes('controller') || importPath.includes('stimulus')) continue;
    // Skip type-only imports
    if (m[0].startsWith('import type')) continue;
    componentImports.push(m[0]);
  }

  // Find controller registrations: { name: 'controller-name', controller: VarName }
  const controllers = [];
  const controllerRegex = /{\s*name:\s*['"]([^'"]+)['"]\s*,\s*controller:\s*(\w+)\s*}/g;
  while ((m = controllerRegex.exec(source)) !== null) {
    const controllerName = m[1];
    const controllerVar = m[2];
    // Find the import path for this controller var
    const ctrlImportRegex = new RegExp(`import\\s+${controllerVar}\\s+from\\s+['"]([^'"]+)['"]`);
    const ctrlMatch = source.match(ctrlImportRegex);
    if (ctrlMatch) {
      controllers.push({
        name: controllerName,
        varName: controllerVar,
        importPath: ctrlMatch[1].replace(/^\.\//, ''),
      });
    }
  }

  pluginData.push({ name, componentImports, controllers });
}

// ---- Generate SSR registry (slots + headScripts, NO controllers) ----
// Import the manifest but the controller import won't be pulled in because
// we use a separate SSR-safe entry point.
const ssrLines = [
  '// AUTO-GENERATED by scripts/generate-plugin-registry.js — do not edit manually.',
  '// SSR-only: slots + headScripts. Controllers are in plugin-controllers.ts.',
  '',
  "import type { PluginManifest } from '../plugins/types';",
  '',
];

// For SSR, we import the manifest but strip controllers at runtime.
// To avoid pulling controller code into the SSR bundle, we import a thin
// SSR-safe wrapper that re-exports without the controller import.
// Actually the simplest approach: generate SSR entry files per plugin.
for (const pd of pluginData) {
  const v = varName(pd.name);
  // Generate an SSR-safe wrapper that imports only SSR parts
  const wrapperPath = resolve(outDir, `${v}_ssr.ts`);
  const wrapperLines = [
    '// AUTO-GENERATED SSR-safe plugin wrapper — no controller imports.',
    '',
  ];

  // Re-import only the component/headScript imports from the plugin index
  // Parse the manifest source more carefully
  const indexSource = readFileSync(resolve(pluginsDir, pd.name, 'index.ts'), 'utf-8');

  // Copy all non-controller imports
  const importLines = indexSource.split('\n').filter(line => {
    if (!line.trim().startsWith('import ')) return false;
    if (line.includes('controller') || line.includes('Controller')) return false;
    if (line.includes("from '../types'")) {
      // Change path since we're in generated/
      return false; // We'll add our own
    }
    return true;
  }).map(line => {
    // Fix relative paths: ../plugins/X/ → ../../src/plugins/X/ → ../plugins/X/
    return line.replace(/from\s+['"]\.\//, `from '../plugins/${pd.name}/`).replace(/from\s+['"]\.\.\//, `from '../`);
  });

  wrapperLines.push("import type { PluginManifest } from '../plugins/types';");
  wrapperLines.push(...importLines);
  wrapperLines.push('');

  // Extract slots and headScripts blocks from the source (without controllers)
  // Rebuild the manifest without the controllers property
  const slotsMatch = indexSource.match(/slots:\s*\[([\s\S]*?)\],/);
  const headScriptsMatch = indexSource.match(/headScripts:\s*\[([\s\S]*?)\],\n\};/s);

  wrapperLines.push(`const manifest: PluginManifest = {`);
  wrapperLines.push(`  name: '${pd.name}',`);
  if (slotsMatch) {
    wrapperLines.push(`  slots: [${slotsMatch[1]}],`);
  }
  if (headScriptsMatch) {
    wrapperLines.push(`  headScripts: [${headScriptsMatch[1]}],`);
  }
  wrapperLines.push('};');
  wrapperLines.push('');
  wrapperLines.push('export default manifest;');
  wrapperLines.push('');

  writeFileSync(wrapperPath, wrapperLines.join('\n'));

  ssrLines.push(`import ${v} from './${v}_ssr';`);
}

ssrLines.push('');
ssrLines.push(`export const plugins: PluginManifest[] = [${pluginData.map(pd => varName(pd.name)).join(', ')}];`);
ssrLines.push('');

// ---- Generate client controllers (direct imports — no JSX) ----
const clientLines = [
  '// AUTO-GENERATED by scripts/generate-plugin-registry.js — do not edit manually.',
  '// Client-side only: Stimulus controllers from plugins. No JSX components included.',
  '',
];

const controllerEntries = [];
for (const pd of pluginData) {
  for (const ctrl of pd.controllers) {
    const alias = varName(pd.name) + '_' + ctrl.varName;
    clientLines.push(`import ${alias} from '../plugins/${pd.name}/${ctrl.importPath}';`);
    controllerEntries.push(`  { name: '${ctrl.name}', controller: ${alias} },`);
  }
}

clientLines.push('');
clientLines.push('export const pluginControllers = [');
clientLines.push(...controllerEntries);
clientLines.push('];');
clientLines.push('');

// Write files
mkdirSync(outDir, { recursive: true });
writeFileSync(resolve(outDir, 'plugin-registry.ts'), ssrLines.join('\n'));
writeFileSync(resolve(outDir, 'plugin-controllers.ts'), clientLines.join('\n'));

console.log(`Generated plugin registry: ${pluginData.length} plugins → src/generated/plugin-registry.ts`);
